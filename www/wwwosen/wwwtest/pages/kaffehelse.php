<p>
	Et simpelt forsøk viser at en kopp med varm kaffe kan frembringe varme følelser for andre mennesker.<br>
	Forsøket gikk ut på at to grupper fikk beskrevet en fiktiv person sine egenskaper. Den ene gruppen<br>
	ble servert varm kaffe, og den andre iskaffe. Forsøkspersonene skulle så beskrive sitt inntrykk av<br>
	den fiktive personen. Det viste seg at gruppen som hadde fått varm kaffe i større grad beskrev personen<br>
	som et varmt menneske, mens den andre gruppen var litt mer skeptiske til personen. <a href="http://www.forskning.no/artikler/2008/oktober/198734">(kilde)</a>
