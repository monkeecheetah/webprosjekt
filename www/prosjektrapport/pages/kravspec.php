			<h1>Kravspesifikasjon for gruppe 29</h1>
			<p class="linkClass"><a href="forprosjekt.html">Forprosjekt</a></p>
			<h2>Problemstilling</h2>
			<h3>Kan vi ved hjelp av en nettsides virkemidler inspirere en kaffedrikkers forhold til kaffe?</h3>
			<h2>Målgruppe:</h2>
			<h2>Typisk norsk kaffedrikker som bare ikke enda vet at han/hun ønsker å lære mer om kaffe.</h2>
			<p>Inspirere halvengasjerte kaffedrikkere til å få et bedre forhold til kaffen sin og utforske nye måter å nyte sin kaffe på</p>
			<h2>Innhold</h2>
			<ul>
				<li>Informasjon om kaffe</li>
				<li>Kaffeknapp - trenger jeg en kaffe nå</li>
				<li>Kaffevelger - en kjapp guide til å finne hvilken kaffe man kan ordne seg</li>
				<li>Parallax effekt for glidende overgang mellom innhold/historier</li>
				<li>Få kaffetips</li>
				<li>Se fine bilder av kaffe</li>
				<li>Trenger jeg en kaffe nå knapp?</li>
				<li>Hvilken kaffe skal jeg lage applikasjon</li>
			</ul>
			<h2>Virkemidler</h2>
			<ul>
				<li>Bruke humor og lett språk for å lokke leseren til å bli mer glad i kaffe</li>
				<li>Passe på at nettsiden taler til målgruppen og ikke blir ovenifra og ned</li>
				<li>Store bilder relatert til kaffe</li>
				<li>Parallax side hvor man får mer lyst på kaffe jo mer man scroller</li>
			</ul>

			<h2>Tekniske krav</h2>
			<ul>
				<li>PHP - for å organisere de forskjellige delene av nettsiden</li>
				<li>jQuery for parallax og animasjonseffekt</li>
				<li>Parallax effekt fordi vi blir veldig inspirerte selv når vi er på den typen nettsteder og ser det kan være et fint verktøy for å skape inspirerende sider</li>
				<li>Respektere opphavsrett - henvise til kilder og bruke bilder vi har lov til å bruke f.eks. egne eller med en CC-lisens</li> 
				<li>jQuery med evt plugins for å lettere implementere parllax effekten og gjøre animasjoner</li>
				<li>Støtte for mobil</li>
				<li>Universal utforming (hovedinnholdet skal være tilgjengelig for alle)</li>
			</ul>
		</article>