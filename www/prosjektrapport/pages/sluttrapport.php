					<p>For å få til dette ville vi prøve å få til en parallax-funksjon på siden. Parallax er forskyvningen,
					eller forskjell i posisjonen av et objekt sett langs to forskjellige synsvinkler. Overført til denne sammenheng betyr det at
					bakgrunn og innhold beveges med forskjellig hastighet og/eller retning.</p>
					<h4>3.2.3 Vår tolkning av parallax</h4>
					<p>Vi opplever at de viktigste elementene er en lang scrolleside, store bakgrunnsbilder, enkelt oppstykket tekst og interaksjonselementer. Teksten skal være lettleselig med viktige ord uthevet. Dette og en kontinuerlig historie opplever vi som suksessfaktorer for å få et godt parallax-nettsted.</p>
					<h4>3.2.4 Jarallax-biblioteket</h4>
					<p>Jarallax er et javascript-bibliotek som skal i utgangspunktet gjøre det enkelt å få til parallax-effekten. Vi valgte derfor å prøve ut dette og fikk det whitelistet. Vi fikk til noen spennende funksjoner, for eksempel at bakgrunnen bevegde seg mye tregere enn teksten, samtidig som det var overliggende bilder som beveget seg i motsatt retning med en helt annen hastighet.</p>
					<h4>3.2.5 Utfordringer med Jarallax</h4>
					<p>Etter hvert som vi utvidet siden med mer innhold viste det seg imidlertid at vi mistet kontrollen over funksjonen. Det ble
					vanskelig å beregne hvor de forskjellige visuelle elementene skulle starte og hvordan hastigheten skulle beregnes. Etter en mer
					grundig undersøkelse av koden i biblioteket, fant vi ut at det ikke var optimalisert for vårt bruksområde. Hovedgrunnen til dette
					er at vi ville ha en lengre sammenhengende nettside, med mange bilder, hvor alt innhold befant seg på en og samme side, med forskjellige bakgrunnsbilder. Jarallax fungerer best når det brukes på en mindre side, med bare to eller tre bakgrunnsbilder. Vi fant også at biblioteket var utdatert og bygget på et eldre jQuery-bibliotek. Vi valgte derfor å skrote hele Jarallax biblioteket og ta oppgaven i egne hender.</p>
