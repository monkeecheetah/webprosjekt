<section id="resultat">
	<h2>4. Resultat</h2>
	<div id="presentasjon">
		<h3>4.1 Presentasjon av nettsiden</h3>
		<p>Vi ville bygge opp nettsiden slik at vi holdt på lytterens interesse så lenge som mulig. Derfor valgte vi å
		lage en lang side framfor å lage flere undersider. Den er delt opp i bolker med forskjellig innhold og hver bolk 
		har sitt eget bakgrunnsbilde. Rekkefølgen på bolkene er bestemt for å få best mulig dramaturgi. Siden begynner med
		en rekke innledende tekster, brytes opp av applikasjonen Kaffevelger og  med bolker som inneholder tyngre
		faktainformasjon.</p>
	</div>

	<div id="scroll">
		<h3>4.2 Scrollefunksjon</h3>
		<p> CHRISTER Forklare hvordan vi har laget scrollingen</p>
	</div>

	<div id="kaffevelger">
		<h3>4.3 Kaffevelger</h3>
		<p> KRISTOFFER Forklare hvordan kaffevelgeren funker</p>
		<p>Kaffevelgeren består av skjemaelementer med radioknapper og checkboxes. All informasjon om tilberedningsmetodene er lagret i JSON fil. jQuery tar seg av sorteringen ut fra verdiene som blir satt med radioknappene. Sorteringen fungerer slik at verdt valg har en verdi og de sammenliknes med verdiene hver metode har. Det ble lagt til slingringsmonn fordi under testing opplevde vi at noen av mulighetene nesten aldri dukket opp, så med slingringsmonn blir det flere resultater.</p>
		<p>Koden ble skrevet mye fra egen erfaring med jQuery og oppslag i jQuery sitt bibliotek kombinert med å studere mange forskjellige eksempler</p>
	</div>

	<div id="design">
		<h3>4.4 Design</h3>
		<p>Vi har valgt å holde oss på en stilren linje når det gjelder designet på siden. Hovedsaklig gjør vi
		 dette ved å holde tekst og bakgrunnsbilder enkle. Bakgrunnsbildene har enkle motiver og teksten, bortsett 
		 fra overskrifter, har fonten Arial, som har lite dekorasjoner. På overskrifter har vi valgt å bruke en font 
		 fra Google Fonts vi følte ville passe med resten av designet på siden. Den er enkel på samme måte som Arial
		 men litt høyere og slankere. På de bakgrunnsbildene med klare motiver er teksten plassert over den delen av bildet hvor 
		 motivet ikke ligger. På de bakgrunnsbildene med mer tekstur-orienterte motiver er teksten plassert i bolker med 
		 bakgrunnsfarge for å skape kontrast. </p>
	</div>


	<div id="bildene">
		<h3>4.5 Bilder</h3>
		<p>Bakgrunnsbildene vi har valgt er funnet på Flickr.com sitt område for bilder med Creative Commons-lisens.
		 Creative Commons-lisens betyr i hovedsak at eier gir andre rettighet til å bruke bildene sine. Det finnes en 
		 del forskjellige typer CC-lisens som blandt annet hindrer bildene i å bli brukt i komersielle formål. Vi bruker bare 
		 med de mest tilgjengelige lisenene for å være sikker. Ettersom at bildene skulle være bakgrunnsbilder, har vi valgt 
		 enkle bilder. Noen har klare motiver, andre er bilder av tekstur vi tenkte passe bra som bakgrunn. Noen av bildene med klare motiver
		 har vi redigert slik at teksten skulle få en glatt bakgrunn å rulle over(gjelder spesielt de to øverste bildene. Se link til orginalbilde i bildebanken).
		 <br>HEGE<br>RELEVANT TEKST OM HEGE SINE BILDER!!! </p>
	</div>
	<div id="responsivt">
		<h3>4.6 Responsivt design</h3>
		<p> CHRISTER/KRISTOFFERHva har vi gjort for at nettsiden skal funke på alle sjermer, i alle browsere</p>
		<p>Responsive/responsible design. Vi har så godt det lar seg gjøre ta hensyn til alle typer størrelser på skjermer. Størrelser er i minst mulig grad oppgitt i piksler og heller brukt prosent. Vi har benyttet oss av media queries for at innholdet skal vises bedre på små skjermer. Bilder er laget i to eller tre størrelser, alt etter behov på siden. Dette for å spare minne og båndbredde på mobile enheter og gjøre siden mer brukervennlig ved at den laster lettere.</p>
	</div>
	<div id="easteregg">
		<h3>4.7 Easteregget</h3>
		<p>Vi gikk for å lage et spill. En enkel pacman klone som blir aktivert ved hjelp av konami-koden. En spillkode for å starte et spill virker helt naturlig. Spillet krever at man samler 20 bønner med kaffekoppen. Når man har oppnådd 20 poeng blir siden erstattet med en video av hvordan en kopp Aeropress lages. Om det ikke er støtte for H.264 i nettleseren blir det lastet en Youtube video istendenfor og evt. en tekst med beskrivelse av videoen om man ikke kan laste inn det heller.</p>
	</div>	
