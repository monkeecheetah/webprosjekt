<h2>Kaffe og koffein</h2>
<p>Kaffe inneholder ca. 110mg koffein i en kopp på 236ml. Avhenger av type, størrelse på kopp osv. Men mer enn i en boks red bull som bare inneholder 80mg i en 250ml boks.
