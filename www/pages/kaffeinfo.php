<h3>To legender.</h3> 
<p>Ene om gjeteren Kaldi. Etiopisk gjeter som oppdaget at geitene ikke sov etter å spist røde bær. Når det ble presentert for det lokale klosteret ble det kalt djevelens verk, kastet i peisen. Det skulle så kastes og ble brukt kaldt vann for å kjøle de ned, som så ga en såpass god lukt at de måtte prøve det. Den andre er om  Sheik Abou'l Hasan Schadheli' sin disippel Omar som siden ble helgen for innføringen av kaffe.</p>
<p>Det er også sagt at kaffebønnene sin første bruk var at de ble spist hele og råe, eller blandet med dyrefett og spist på lange turer siden 5-600 tallet i Etiopia.</p>

