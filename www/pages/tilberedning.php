<h2>Prezzkanne</h2>
	<div id="pressinfo">

	 	<p>Presskanne er en rask og enkel måte å lage mye og fyldig kaffe på.<br>
	 	   Du kan bruke mange forskjellige typer kaffe i en presskanne.<br>
	 	   Både <em>filter</em>- og <em>kokmalt</em> funker. Du kan også kjøpe<br>
	 	   kaffe som er kvernet spesielt for presskanne. Den beste veien til god<br>
	 	   presskaffe er likevel å kjøpe hele bønner og kverne dem selv. <br>
	 	   Kvern bønnene grovt, så de ikke slipper igjennom metallfilteret<br>
	 	</p>
 	</div>

 	<div id="presstips">
		<p>En kanne kan du lage lage enkelt, ved å hive oppi en "fornuftig" mengde kaffe,<br>
		   kokende vann og presse ned etter et par minutter. Hvis du vi eksprimentere<br>
		   litt mer med smaken kan du være litt mer nøye. Kaffenerder og barristaer har<br>
		   laget kaffe i presskanne i mangemange år og har funnet ut hva som funker best.<br>
		   Her er noen tips:
		</p>
		<ul>
			<li>Skal du lage en hel presskanne, som vanligvis er en liter, bør du bruke rundt 60 gram bønner. </li>
			<li>Kannen burde du forvarme med varmt vann før du har kaffen i.</li>
			<li>Vannet du bruker skal ikke koke, men være rundt 90-96 grader</li>
			<li>Hell i vannet i to runder. Fyll kannen halveis opp først og rør rundt, før du heller i resten av vannet.</li>
			<li>La kaffe kose seg i ganske nøyaktig 4 min og press til slutt ned stempelet.</li>
			<li>Drikk med en gang!</li>

		</ul>
	</div>

<h2>Espresso</h2>
	<p>Drikken espresso er laget ved å tvinge nesten kokende vann gjennom fint<br>
	  	malte kaffebønner. Kaffesjapper bruker en espressomaskin for å gjøre dette,<br>
	  	men skal du enkelt lage espresso hjemme, kan du bruke en <strong>mokkakanne</strong>.
	  	Når man lager espresso bruker man kaffe som er veldig finmalt.<br>
	  	Det kan man kjøpe ferdig kvernet i butikken, eller man kan<br>
	  	kjøpe hele bønner selv og kverne de hjemme.
	</p>

	 
	

	<h3>Mokkakannen</h3>
	<p>
		En mokkakanne koster rundt 300-400 kroner og kan kjøpes i forskjellige<br>
		størrelser i de fleste butikker som selger kjøkkenutstyr.<br>
		En mokkakanne funker slik at du heller vann i den nederste delen,<br>
		fyller filterdelen med kaffe, skrur på toppdelen og setter den på<br>
		stekeplaten. Trykket som skapes når vannet koker i den nederste<br>
		delen presser vannet opp til kaffen og videre opp i den øverste delen.<br>
		Nå den øverste halvdelen er halvfull, kan du ta den av platen. 
	</p>


